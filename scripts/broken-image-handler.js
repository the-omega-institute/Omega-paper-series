// Omega showcase — handle broken media assets gracefully
// Replaces broken infographic/video links with "coming soon" placeholders
document.addEventListener('DOMContentLoaded', function() {
  // Handle broken infographic images
  document.querySelectorAll('img').forEach(function(img) {
    img.addEventListener('error', function() {
      img.classList.add('broken-image');
      img.alt = 'Media asset is still being generated';
    });
  });

  // Handle broken video sources
  document.querySelectorAll('video').forEach(function(video) {
    video.addEventListener('error', function(e) {
      var placeholder = document.createElement('div');
      placeholder.className = 'video-placeholder';
      placeholder.innerHTML = '🎬 <em>Video is still being generated by NotebookLM.</em> <br>Check back in a few minutes.';
      placeholder.style.cssText = 'padding: 3rem; text-align: center; border: 2px dashed #ddd; border-radius: 8px; margin: 1.5rem 0; color: #888;';
      if (video.parentNode) {
        video.parentNode.replaceChild(placeholder, video);
      }
    }, true);

    // Also check on source failure (multiple <source> tags)
    video.querySelectorAll('source').forEach(function(src) {
      src.addEventListener('error', function() {
        video.dispatchEvent(new Event('error'));
      });
    });
  });
});
